def main():
    """
    This function prints a countdown from 10 to 1 and then prints 'Liftoff!'.
    """
    for i in range(10, 0, -1):  # Loop from 10 down to 1
        print(i)
    print("Liftoff!")  # Print liftoff after the countdown



if __name__ == '__main__':
    main()


